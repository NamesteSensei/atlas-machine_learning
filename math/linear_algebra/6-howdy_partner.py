#!/usr/bin/env python3
"""
This module contains a function to concatenate two arrays.
"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays.

    Args:
        arr1 (list): First array.
        arr2 (list): Second array.

    Returns:
        list: A new array with elements of arr1 followed by elements of arr2.
    """
    return arr1 + arr2
