#!/usr/bin/env python3
"""
This module contains a function to concatenate two arrays.
"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays.

    Args:
        arr1 (list): The first array.
        arr2 (list): The second array.

    Returns:
        list: A new array containing elements of arr1 followed by arr2.
    """
    return arr1 + arr2
